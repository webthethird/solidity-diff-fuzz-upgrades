DEPLOY_COMPOUND_WITH_SUFFIX () {
    local SUFFIX=$1
    local CONTRACT="UNITROLLER"
    local GANACHE_KEY="0xf2f48ee19680706196e2e339e5da3491186e0c4c5030670656b0e0164837257d"
    echo "# Deploying Compound Protocol to etheno.."
    cd ./compound-eureka
    # Use foundry to deploy contracts via etheno
    CONTRACT_ADDRESS=$(echo "y" | yarn eureka apply -n development -b ./.build -c config/*.js -e eureka/{compound,testnet,testnet-gov,ropsten,2_ropsten,3_ropsten}.eureka | grep "Contract Unitroller deployed to")
    CONTRACT_ADDRESS=${CONTRACT_ADDRESS#Contract Unitroller deployed to 0x}
    mv ./state/development-state.json ./state/development-state-${SUFFIX}.json
    mv ./networks/development.json ./networks/development-${SUFFIX}.json
    cd ..
    echo "address constant ${CONTRACT}_${SUFFIX}_ADDR = address(0x00$CONTRACT_ADDRESS);" >> /tmp/addresses.sol.tmp # we don't get addresses with valid checksums from forge, workaround with 00 prefix
    FAUCET_ADDRESS=$(cat ./compound-eureka/networks/development-${SUFFIX}.json | python3 -c "import sys, json; print(json.loads(sys.stdin.read())['Contracts']['Fauceteer'])")
    FAUCET_ADDRESS=${FAUCET_ADDRESS#0x}
    echo "address constant FAUCET_${SUFFIX}_ADDR = address(0x00$FAUCET_ADDRESS);" >> /tmp/addresses.sol.tmp
    RESERVOIR_ADDRESS=$(cat ./compound-eureka/networks/development-${SUFFIX}.json | python3 -c "import sys, json; print(json.loads(sys.stdin.read())['Contracts']['Reservoir'])")
    RESERVOIR_ADDRESS=${RESERVOIR_ADDRESS#0x}
    echo "address constant RESERVOIR_${SUFFIX}_ADDR = address(0x00$RESERVOIR_ADDRESS);" >> /tmp/addresses.sol.tmp
    COMP_ADDRESS=$(cat ./compound-eureka/networks/development-${SUFFIX}.json | python3 -c "import sys, json; print(json.loads(sys.stdin.read())['Contracts']['Comp'])")
    echo "# Transferring COMP to faucet and reservoir..."
    cast send --legacy --private-key $GANACHE_KEY $COMP_ADDRESS "transfer(address,uint256)" 0x$FAUCET_ADDRESS 1000000000000000000000000
    cast send --legacy --private-key $GANACHE_KEY $COMP_ADDRESS "transfer(address,uint256)" 0x$RESERVOIR_ADDRESS 4000000000000000000000000
}
UPGRADE_COMPOUND_WITH_VERSION_KEY () {
    local NETWORK_FILE=$1
    local VERSION_KEY=$2
    local GANACHE_KEY="0xf2f48ee19680706196e2e339e5da3491186e0c4c5030670656b0e0164837257d"
    UNITROLLER_ADDRESS=$(cat $NETWORK_FILE | python3 -c "import sys, json; print(json.loads(sys.stdin.read())['Contracts']['Unitroller'])")
    VERSION_ADDRESS=$(cat $NETWORK_FILE | python3 -c "import sys, json; print(json.loads(sys.stdin.read())['Contracts']['${VERSION_KEY}'])")
    echo "# Upgrading Unitroller at $UNITROLLER_ADDRESS to implementation at $VERSION_ADDRESS..."
    cast send --legacy --private-key $GANACHE_KEY $UNITROLLER_ADDRESS "_setPendingImplementation(address)(uint256)" $VERSION_ADDRESS
    cast send --legacy --private-key $GANACHE_KEY $VERSION_ADDRESS "_become(address)" $UNITROLLER_ADDRESS
}
